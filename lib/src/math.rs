pub fn add_two_numbers(a: f64, b: f64) -> String {
  let sum: f64 = a + b;
  sum.to_string()
}
pub fn subtract_two_numbers(a: f64, b: f64) -> String {
  let sum: f64 = a - b;
  sum.to_string()
}
pub fn multiply_two_numbers(a: f64, b: f64) -> String {
  let sum: f64 = a * b;
  sum.to_string()
}
pub fn divide_two_numbers(a: f64, b: f64) -> String {
  let sum: f64 = a / b;
  sum.to_string()
}
pub fn get_remainder(a: f64, b: f64) -> String {
  let sum: f64 = a % b;
  sum.to_string()
}
